export const Abbreviation = (name: string) => {
  if (name === "BRL") {
    return "Reais";
  }
  if (name === "USD") {
    return "Dólares dos EUA";
  }
  if (name === "EUR") {
    return "Euros";
  }
  if (name === "GBP") {
    return "Libras esterlina";
  }
  if (name === "CAD") {
    return "Dólares canadense";
  }
  if (name === "AUD") {
    return "Dólares australiano";
  }
  if (name === "JPY") {
    return "Ienes Japonês";
  }
  if (name === "INR") {
    return "Rúpias indiana";
  }
  if (name === "NZD") {
    return "Dólares neozelandês";
  }
  if (name === "CHF") {
    return "Francos Suíço";
  }
  if (name === "ZAR") {
    return "Rands sul-africano";
  }
  if (name === "RUB") {
    return "Rublos russo";
  }
};
